int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int a;

	i = 0;
	a = 0;
	while (tab[i] != '\0')
	{
		if ((*f)(tab[i]) == 1)
			a++;
		i++;
	}
	return (a);
}
